package es.ehu.oms.CPTMLTool.model;

public class Passwords {
	
	private String pwdMMD;
	private String pwdMateo;
	private String pwdAntiCa;
	
	
	public String getPwdMMD() {
		return pwdMMD;
	}
	public void setPwdMMD(String pwdMMD) {
		this.pwdMMD = pwdMMD;
	}
	public String getPwdMateo() {
		return pwdMateo;
	}
	public void setPwdMateo(String pwdMateo) {
		this.pwdMateo = pwdMateo;
	}
	public String getPwdAntiCa() {
		return pwdAntiCa;
	}
	public void setPwdAntiCa(String pwdAntiCa) {
		this.pwdAntiCa = pwdAntiCa;
	}
}
